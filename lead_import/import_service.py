def _normalize_linkedin_url(url: str) -> str:
    """
    Normalize a LinkedIn URL to the same canonical form used by the bulk
    enrichment API (_normalize_linkedin_url in lead_enrichment_brightdata_service).

    Rules (must stay in sync with the service):
      1. Strip whitespace + trailing slash
      2. Prepend https:// if no scheme
      3. Normalize country-specific subdomains (in., nl., uk., de., etc.) → www.
      4. Lowercase the path portion only (preserve domain casing)

    This ensures MD5(normalized_url) matches the lead ID generated by the
    bulk enrichment API for the same person.
    """
    if not url:
        return url
    url = url.strip().rstrip("/")
    if not url.startswith("http"):
        url = f"https://{url}"
    # country subdomain → www  (e.g. https://in.linkedin.com → https://www.linkedin.com)
    url = re.sub(r"https?://[a-z]{2}\.linkedin\.com", "https://www.linkedin.com", url)
    return url


