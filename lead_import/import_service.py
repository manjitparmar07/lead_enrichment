"""
import_service.py
-----------------
High-performance file import — chunked + parallel via Redis worker pool.

Flow per file:
  1. POST /start  →  start_import_job()
       Creates enrichment_jobs record, fires asyncio.create_task(_stage_and_chunk())

  2. _stage_and_chunk()  [background, per file]
       a. Parse file (Python)
       b. COPY all rows → import_stage_{job_id}  (COPY protocol, ~50K rows/sec)
       c. Split staging into CHUNK_SIZE-row tables:
            import_chunk_{job_id}_0, import_chunk_{job_id}_1, ...
       d. RPUSH each chunk task → wb:import:chunks  (Redis)
       e. Drop main staging table
       f. Save unmapped columns → import_unmapped_data

  3. import_worker._worker() × 4  [BLPOP loop]
       Each worker: INSERT SELECT (xmax dedup) → update Redis progress
       Last chunk of a job → _finalise() writes counts to DB

Concurrency limits:
  - asyncpg pool: max 10 connections (shared across all features)
  - COPY_BATCH: 10K rows per COPY call (balances round-trips vs memory)
  - CHUNK_SIZE: 50K rows per Redis chunk task (4 workers × 50K = 200K in parallel)

Deduplication:
  - ON CONFLICT (id) DO UPDATE — existing leads updated in-place
  - xmax = 0  → new insert
  - xmax != 0 → duplicate updated
"""

