async def start_import_job(
    file_bytes: bytes,
    filename:   str,
    mapping:    dict[str, str],
    org_id:     str,
    folder_id:  str | None = None,
) -> str:
    from db import get_pool

    job_id = str(uuid.uuid4())
    fhash  = _file_hash(file_bytes)
    now    = datetime.now(timezone.utc).isoformat()

    async with get_pool().acquire() as conn:
        await conn.execute(
            """
            INSERT INTO enrichment_jobs
                (id, organization_id, status, total_urls, processed, failed,
                 filename, file_hash, folder_id, skipped, new_count, updated_count,
                 created_at, updated_at)
            VALUES ($1, $2, 'pending', 0, 0, 0, $3, $4, $5, 0, 0, 0, $6, $6)
            """,
            job_id, org_id, filename, fhash, folder_id, now,
        )

    asyncio.create_task(
        _stage_and_chunk(file_bytes, filename, fhash, mapping, org_id, job_id),
        name=f"import-{job_id[:8]}",
    )
    return job_id


