async def get_import_history(org_id: str, page: int = 1, limit: int = 30) -> dict:
    from db import get_pool
    offset = (page - 1) * limit
    async with get_pool().acquire() as conn:
        rows = await conn.fetch(
            """
            SELECT id, filename, file_hash, folder_id, status,
                   total_urls AS total, processed, new_count, updated_count, skipped, failed,
                   error, created_at, updated_at
            FROM enrichment_jobs
            WHERE organization_id = $1 AND filename IS NOT NULL
            ORDER BY created_at DESC
            LIMIT $2 OFFSET $3
            """,
            org_id, limit, offset,
        )
        total = await conn.fetchval(
            "SELECT COUNT(*) FROM enrichment_jobs WHERE organization_id=$1 AND filename IS NOT NULL",
            org_id,
        )
    return {"history": [dict(r) for r in rows], "total": total, "page": page, "limit": limit}


