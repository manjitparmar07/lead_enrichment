async def get_folder_status(folder_id: str, org_id: str) -> dict:
    from db import get_pool
    async with get_pool().acquire() as conn:
        rows = await conn.fetch(
            """
            SELECT id, filename, status,
                   total_urls AS total, processed, new_count, updated_count, skipped, failed, error,
                   created_at, updated_at
            FROM enrichment_jobs
            WHERE folder_id=$1 AND organization_id=$2
            ORDER BY created_at ASC
            """,
            folder_id, org_id,
        )

    jobs = []
    for r in rows:
        d   = dict(r)
        tot = int(d["total"] or 0)
        prc = int(d["processed"] or 0)
        d["pct"] = round(prc / tot * 100) if tot else 0
        jobs.append(d)

    total_files = len(jobs)
    completed   = sum(1 for j in jobs if j["status"] == "completed")
    failed      = sum(1 for j in jobs if j["status"] == "failed")
    running     = sum(1 for j in jobs if j["status"] in ("running", "pending"))

    return {
        "folder_id":    folder_id,
        "total_files":  total_files,
        "completed":    completed,
        "failed":       failed,
        "running":      running,
        "done":         (completed + failed == total_files) and total_files > 0,
        "total_new":    sum(j.get("new_count",     0) or 0 for j in jobs),
        "total_updated":sum(j.get("updated_count", 0) or 0 for j in jobs),
        "total_skipped":sum(j.get("skipped",       0) or 0 for j in jobs),
        "jobs":         jobs,
    }
