def _build_col_maps(
    headers: list[str],
    mapping: dict[str, str],
) -> tuple[dict[int, str], dict[int, str]]:
    col_to_field:   dict[int, str] = {}
    col_to_unmapped: dict[int, str] = {}
    for i, h in enumerate(headers):
        field = mapping.get(h, "")
        if field and field in IMPORTABLE_FIELDS:
            col_to_field[i] = field
        elif h.strip():
            col_to_unmapped[i] = h
    return col_to_field, col_to_unmapped


def _build_row(
    row: list[str],
    col_to_field: dict[int, str],
    col_to_unmapped: dict[int, str],
) -> tuple[dict[str, str], dict[str, str]]:
    mapped:   dict[str, str] = {}
    unmapped: dict[str, str] = {}
    for idx, field in col_to_field.items():
        val = row[idx].strip() if idx < len(row) else ""
        if val:
            mapped[field] = val
    for idx, col_name in col_to_unmapped.items():
        val = row[idx].strip() if idx < len(row) else ""
        if val:
            unmapped[col_name] = val
    return mapped, unmapped


def _to_staging_tuple(record: dict[str, str], row_uuid: str) -> tuple:
    return (row_uuid,) + tuple(record.get(col) or None for col in _DATA_COLS)


# ─────────────────────────────────────────────────────────────────────────────
# Redis helper
# ─────────────────────────────────────────────────────────────────────────────

async def _get_redis() -> Any:
    import redis.asyncio as aioredis
    return aioredis.from_url(REDIS_URL, decode_responses=True)


# ─────────────────────────────────────────────────────────────────────────────
# DB update helper
# ─────────────────────────────────────────────────────────────────────────────

async def _upd_job(job_id: str, **kw: Any) -> None:
    from db import get_pool
    keys = list(kw.keys())
    sets = ", ".join(f"{k}=${i+1}" for i, k in enumerate(keys))
    args = [kw[k] for k in keys] + [job_id]
    async with get_pool().acquire() as conn:
        await conn.execute(f"UPDATE enrichment_jobs SET {sets} WHERE id=${len(args)}", *args)


# ─────────────────────────────────────────────────────────────────────────────
# Background worker: parse → COPY → split chunks → push to Redis
# ─────────────────────────────────────────────────────────────────────────────

async def _stage_and_chunk(
    file_bytes: bytes,
    filename:   str,
    file_hash:  str,
    mapping:    dict[str, str],
    org_id:     str,
    job_id:     str,
) -> None:
    from db import get_pool

    pool = get_pool()
    now  = datetime.now(timezone.utc).isoformat()
    staging_base = f"import_stage_{job_id.replace('-', '_')}"

    try:
        await _ensure_schema()
        await _upd_job(job_id, status="running", updated_at=now)

        # ── 1. Parse file ────────────────────────────────────────────────────
        _, headers, rows = _load_file(file_bytes, filename)
        total = len(rows)
        await _upd_job(job_id, total_urls=total, updated_at=now)
        logger.info("[import %s] parsed %d rows from '%s'", job_id[:8], total, filename)

        col_to_field, col_to_unmapped = _build_col_maps(headers, mapping)
        if not col_to_field:
            await _upd_job(job_id, status="failed", error="No valid column mapping provided", updated_at=now)
            return

        # ── 2. Build staging tuples ──────────────────────────────────────────
        staging_tuples: list[tuple] = []
        unmapped_tuples: list[tuple] = []
        skipped = 0

        for row in rows:
            mapped, unmapped = _build_row(row, col_to_field, col_to_unmapped)
            # Normalize LinkedIn URL so MD5-based lead ID matches the bulk enrichment API
            if mapped.get("linkedin_url"):
                mapped["linkedin_url"] = _normalize_linkedin_url(mapped["linkedin_url"])
            has_key = mapped.get("linkedin_url") or mapped.get("work_email") or mapped.get("personal_email")
            if not has_key:
                skipped += 1
                continue

            row_uuid = str(uuid.uuid4())
            lead_id  = mapped.get("linkedin_url") or row_uuid
            staging_tuples.append(_to_staging_tuple(mapped, row_uuid))

            if unmapped:
                unmapped_tuples.append((
                    str(uuid.uuid4()), lead_id, org_id, filename, job_id,
                    json.dumps(unmapped, ensure_ascii=False), now,
                ))

        if not staging_tuples:
            await _upd_job(job_id, status="completed", processed=0, skipped=skipped,
                           error=f"All {skipped} rows skipped — no linkedin_url/email",
                           updated_at=now)
            return

        n_valid = len(staging_tuples)
        logger.info("[import %s] %d valid rows, %d skipped, splitting into %d chunks of %d",
                    job_id[:8], n_valid, skipped, math.ceil(n_valid / CHUNK_SIZE), CHUNK_SIZE)

        # ── 3. COPY all rows → main staging table ────────────────────────────
        async with pool.acquire() as conn:
            await conn.execute("SET synchronous_commit = OFF")
            await conn.execute(f"DROP TABLE IF EXISTS {staging_base}")
            await conn.execute(f"CREATE UNLOGGED TABLE {staging_base} ({_STAGING_DDL})")

            copied = 0
            for i in range(0, n_valid, COPY_BATCH):
                batch = staging_tuples[i: i + COPY_BATCH]
                await conn.copy_records_to_table(staging_base, records=batch, columns=_STAGING_COLS)
                copied += len(batch)
                # 0–40 % progress during COPY phase
                await _upd_job(job_id, processed=int(copied / n_valid * total * 0.4), updated_at=now)

        logger.info("[import %s] COPY done (%d rows) — splitting into chunks", job_id[:8], n_valid)

        # ── 4. Split main staging → chunk staging tables + push to Redis ─────
        n_chunks = max(1, math.ceil(n_valid / CHUNK_SIZE))
        r = await _get_redis()

        for chunk_idx in range(n_chunks):
            chunk_staging = f"import_chunk_{job_id.replace('-', '_')}_{chunk_idx}"
            offset = chunk_idx * CHUNK_SIZE
            async with pool.acquire() as conn:
                await conn.execute(f"DROP TABLE IF EXISTS {chunk_staging}")
                await conn.execute(
                    f"CREATE UNLOGGED TABLE {chunk_staging} AS "
                    f"SELECT * FROM {staging_base} LIMIT {CHUNK_SIZE} OFFSET {offset}"
                )

            await r.rpush(QUEUE_KEY, json.dumps({
                "job_id":       job_id,
                "chunk_idx":    chunk_idx,
                "total_chunks": n_chunks,
                "staging":      chunk_staging,
                "org_id":       org_id,
                "filename":     filename,
                "now":          now,
            }))

        # Initialise Redis job progress tracker
        await r.hset(JOB_KEY_TPL.format(job_id=job_id), mapping={
            "done":          0,
            "total_chunks":  n_chunks,
            "total":         total,
            "new_count":     0,
            "updated_count": 0,
            "processed":     0,
            "skipped":       skipped,
        })
        await r.aclose()

        # Drop main staging (chunk tables remain for workers to consume)
        async with pool.acquire() as conn:
            await conn.execute(f"DROP TABLE IF EXISTS {staging_base}")

        logger.info("[import %s] %d chunk tasks pushed to Redis queue", job_id[:8], n_chunks)

        # ── 5. Save unmapped data ────────────────────────────────────────────
        if unmapped_tuples:
            async with pool.acquire() as conn:
                await conn.copy_records_to_table(
                    "import_unmapped_data",
                    records=unmapped_tuples,
                    columns=["id", "lead_id", "org_id", "source_file", "job_id", "raw_data", "created_at"],
                )
            logger.info("[import %s] saved %d unmapped rows", job_id[:8], len(unmapped_tuples))

    except Exception as exc:
        err_str = str(exc)
        if "No space left" in err_str or "FileFallocate" in err_str or "ENOSPC" in err_str:
            err_str = (
                "Database disk full — no space left on device. "
                "Free up space or upgrade your database plan, then retry."
            )
        elif "field larger than field limit" in err_str:
            err_str = f"CSV field too large — a cell in your file exceeds the maximum allowed size. {err_str}"
        logger.exception("[import %s] stage_and_chunk failed: %s", job_id[:8], exc)
        try:
            async with pool.acquire() as conn:
                await conn.execute(f"DROP TABLE IF EXISTS {staging_base}")
        except Exception:
            pass
        await _upd_job(job_id, status="failed", error=err_str[:500], updated_at=now)


