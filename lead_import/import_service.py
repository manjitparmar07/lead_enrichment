def _file_hash(data: bytes) -> str:
    return hashlib.md5(data).hexdigest()


def _file_hash_fast(data: bytes) -> str:
    """Fast fingerprint: MD5 of first 64 KB + file size.
    Sufficient for duplicate detection; avoids hashing 500 MB files."""
    h = hashlib.md5(data[:65536])
    h.update(str(len(data)).encode())
    return h.hexdigest()


def _norm(s: str) -> str:
    return s.lower().replace(" ", "").replace("_", "").replace("-", "").replace(".", "")


# ── Full-read helpers (used by _load_file for actual import) ──────────────────

def _read_csv_tsv(data: bytes, delim: str) -> tuple[list[str], list[list[str]]]:
    text = data.decode("utf-8-sig", errors="replace")
    reader = csv.reader(io.StringIO(text), delimiter=delim)
    rows = list(reader)
    if not rows:
        return [], []
    return rows[0], rows[1:]


def _read_excel(data: bytes, filename: str) -> tuple[list[str], list[list[str]]]:
    ext = filename.rsplit(".", 1)[-1].lower()
    if ext == "xls":
        import xlrd
        wb = xlrd.open_workbook(file_contents=data)
        ws = wb.sheet_by_index(0)
        if ws.nrows == 0:
            return [], []
        return (
            [str(ws.cell_value(0, c)) for c in range(ws.ncols)],
            [[str(ws.cell_value(r, c)) for c in range(ws.ncols)] for r in range(1, ws.nrows)],
        )
    else:
        import openpyxl
        wb = openpyxl.load_workbook(io.BytesIO(data), read_only=True, data_only=True)
        ws = wb.active
        all_rows = list(ws.iter_rows(values_only=True))
        wb.close()
        if not all_rows:
            return [], []
        return (
            [str(c) if c is not None else "" for c in all_rows[0]],
            [[str(c) if c is not None else "" for c in r] for r in all_rows[1:]],
        )


def _load_file(data: bytes, filename: str) -> tuple[str, list[str], list[list[str]]]:
    ext = filename.rsplit(".", 1)[-1].lower()
    if ext == "csv":
        return "csv", *_read_csv_tsv(data, ",")
    elif ext == "tsv":
        return "tsv", *_read_csv_tsv(data, "\t")
    elif ext in ("xls", "xlsx"):
        return ext, *_read_excel(data, filename)
    raise ValueError(f"Unsupported file type: .{ext}")


# ── Preview-only fast readers (stop after PREVIEW_ROWS, use metadata for count) ─

PREVIEW_ROWS = 10


def _preview_csv_tsv(data: bytes, delim: str) -> tuple[list[str], list[list[str]], int]:
    """
    Fast preview for CSV/TSV:
    - Row count from raw byte newline scan (C-speed, no full decode)
    - Headers + sample from first 64 KB only (no full file parse)
    """
    # Fast row count: count newlines in raw bytes minus the header line
    total = max(0, data.count(b'\n') - 1)

    # Parse headers + sample from first 64 KB only
    chunk_bytes = data[:65536]
    # Trim to the last complete line so csv.reader sees no truncated row
    last_nl = chunk_bytes.rfind(b'\n')
    if last_nl > 0:
        chunk_bytes = chunk_bytes[:last_nl]
    chunk = chunk_bytes.decode("utf-8-sig", errors="replace")

    reader = csv.reader(io.StringIO(chunk), delimiter=delim)
    try:
        headers = next(reader)
    except StopIteration:
        return [], [], total
    import itertools
    sample = list(itertools.islice(reader, PREVIEW_ROWS))
    return headers, sample, total


def _preview_xlsx(data: bytes) -> tuple[list[str], list[list[str]], int]:
    """Read headers + first 10 rows; use ws.max_row for total (no full iteration)."""
    import openpyxl
    wb = openpyxl.load_workbook(io.BytesIO(data), read_only=True, data_only=True)
    ws = wb.active
    # max_row is stored in sheet metadata — available without iterating all rows
    total = max(0, (ws.max_row or 1) - 1)
    rows_iter = ws.iter_rows(values_only=True)
    try:
        headers = [str(c) if c is not None else "" for c in next(rows_iter)]
    except StopIteration:
        wb.close()
        return [], [], 0
    sample: list[list[str]] = []
    for i, row in enumerate(rows_iter):
        if i >= PREVIEW_ROWS:
            break
        sample.append([str(c) if c is not None else "" for c in row])
    wb.close()
    return headers, sample, total


def _preview_xls(data: bytes) -> tuple[list[str], list[list[str]], int]:
    """Read headers + first 10 rows; use ws.nrows for total."""
    import xlrd
    wb = xlrd.open_workbook(file_contents=data)
    ws = wb.sheet_by_index(0)
    if ws.nrows == 0:
        return [], [], 0
    headers = [str(ws.cell_value(0, c)) for c in range(ws.ncols)]
    sample = [
        [str(ws.cell_value(r, c)) for c in range(ws.ncols)]
        for r in range(1, min(1 + PREVIEW_ROWS, ws.nrows))
    ]
    return headers, sample, ws.nrows - 1


# ─────────────────────────────────────────────────────────────────────────────
# Auto-mapping + preview
# ─────────────────────────────────────────────────────────────────────────────

def auto_suggest_mapping(columns: list[str]) -> dict[str, str]:
    return {col: _NORM_TO_FIELD.get(_norm(col), "") for col in columns}


def parse_preview(file_bytes: bytes, filename: str) -> dict:
    """
    Fast preview — reads only headers + first 10 rows.
    Does NOT load the entire file. Uses metadata for row count.
    """
    ext = filename.rsplit(".", 1)[-1].lower()
    if ext == "csv":
        file_type = "csv"
        headers, sample, total = _preview_csv_tsv(file_bytes, ",")
    elif ext == "tsv":
        file_type = "tsv"
        headers, sample, total = _preview_csv_tsv(file_bytes, "\t")
    elif ext == "xlsx":
        file_type = "xlsx"
        headers, sample, total = _preview_xlsx(file_bytes)
    elif ext == "xls":
        file_type = "xls"
        headers, sample, total = _preview_xls(file_bytes)
    else:
        raise ValueError(f"Unsupported file type: .{ext}")

    return {
        "file_type":         file_type,
        "columns":           headers,
        "rows":              [dict(zip(headers, row)) for row in sample],
        "total_rows":        total,
        "suggested_mapping": auto_suggest_mapping(headers),
        "file_hash":         _file_hash_fast(file_bytes),
    }


